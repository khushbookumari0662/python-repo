text = "KhushbooKumari"

print("Original String:", text)

# from index 0 to 5
print("text[0:6] =", text[0:6])

# from index 6 to end
print("text[6:] =", text[6:])

# from start to index 6
print("text[:6] =", text[:6])

# full string
print("text[:] =", text[:])

# with step
print("text[::2] =", text[::2])

# reverse string
print("text[::-1] =", text[::-1])
